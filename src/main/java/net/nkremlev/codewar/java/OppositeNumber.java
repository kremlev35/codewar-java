package net.nkremlev.codewar.java;

/**
 * @see <a href="https://www.codewars.com/kata/56dec885c54a926dcd001095">Opposite number</a>
 *
 * @author nkremlev
 */
public final class OppositeNumber {

    /**
     * Возвращает число в отрицание
     * @param number число
     * @return число с отрицанием
     */
    public static int opposite(int number) {
        return -number;
    }
}
